SELECT TITLE, LENGTH
  FROM FILM
WHERE LENGTH >= ALL
(
    SELECT MAX(LENGTH)
      FROM FILM A
         , FILM_CATEGORY B 
    WHERE A.FILM_ID = B.FILM_ID
    GROUP BY B.CATEGORY_ID 
);











SELECT RATING, ROUND(AVG (LENGTH), 2)
  FROM FILM
GROUP BY RATING;

















SELECT FILM_ID
     , TITLE
     , LENGTH
  FROM FILM
 WHERE LENGTH > ALL 
(
   SELECT ROUND(AVG (LENGTH), 2)
    FROM FILM
   GROUP BY RATING 
)
ORDER BY LENGTH;

