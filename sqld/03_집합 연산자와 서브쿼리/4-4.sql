SELECT
       DISTINCT INVENTORY.FILM_ID, FILM.TITLE
  FROM
       INVENTORY
INNER JOIN 
       FILM 
ON FILM.FILM_ID = INVENTORY.FILM_ID
ORDER BY FILM.TITLE;

SELECT
       DISTINCT A.FILM_ID, B.TITLE
  FROM
       INVENTORY A
INNER JOIN 
       FILM B
ON B.FILM_ID = A.FILM_ID
ORDER BY B.TITLE;

