SELECT
       FIRST_NAME
     , LAST_NAME
  FROM
       CUSTOMER C
WHERE
EXISTS ( SELECT 1
           FROM PAYMENT P
          WHERE P.CUSTOMER_ID = C.CUSTOMER_ID
            AND P.AMOUNT > 11 
               )
ORDER BY FIRST_NAME, LAST_NAME;












